type UserAgentJson struct {
	Ua             string           `json:"ua"`
	Mozilla        string           `json:"mozilla"`
	Platform       string           `json:"platform"`
	Os             string           `json:"os"`
	OsInfo         useragent.OSInfo `json:"osInfo"`
	Localization   string           `json:"localization"`
	Model          string           `json:"model"`
	BrowserName    string           `json:"browserName"`
	BrowserVersion string           `json:"browserVersion"`
	Bot            bool             `json:"bot"`
	Mobile         bool             `json:"mobile"`
}

type ClickEvent struct {
	EventId       string              `json:"eventId,omitempty"`
	Headers       map[string][]string `json:"headers,omitempty"`
	ClientIp      string              `json:"clientIp,omitempty"`
	UserAgent     string              `json:"userAgent,omitempty"`
	UserAgentJson UserAgentJson       `json:"userAgentJson,omitempty"`
	Url           string              `json:"url,omitempty"`
	DeepLink      string              `json:"deepLink,omitempty"`
	Timestamp     int64               `json:"timestamp,omitempty"`
}

func SinkClickEventToClickhouse(delivery amqp.Delivery) bool {
	clickEvent := &ClickEvent{}
	if err := json.Unmarshal(delivery.Body, clickEvent); err == nil {

		event := TransformClickEventToEventModel(clickEvent)

		if result := clickhouse.Clickhouse(config.New(), nil).Create(event); result != nil && result.Error == nil {
			log.Printf("Published successfully: %v", result.RowsAffected)
			return true
		} else if result == nil {
			log.Printf("Published failed: %v %v", result, event)
		} else {
			log.Printf("Published failed: %v %v", result.Error, event)
		}
	}

	return false
}

